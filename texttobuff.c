void ttb(int *buff, int row, const unsigned char *text,int screenwidth,int textrowlength,int textrowscount){
	int rowinbukva=row%6+3;
	int stroka = (textrowlength*(row/6));
	if(stroka<textrowscount){
		for(int i=0; i<screenwidth;i+=4){
			int bukva = text[i>>2];
			buff[i  ] = ((font[rowinbukva+bukva*6])>>7) & 1;
			buff[i+1] = ((font[rowinbukva+bukva*6])>>6) & 1;
			buff[i+2] = ((font[rowinbukva+bukva*6])>>5) & 1;
			buff[i+3] = ((font[rowinbukva+bukva*6])>>4) & 1;
		}
	}else{
		for(int i=0; i<96;i++){buff[i]=0;}
	}
}
void texttobuff(int *buff, int row){
	ttb(buff, row, htext,96,24,5);
}

void shess(int *buff){
    static int vizovy=0;
    for(int i=0; i<96;i++){
        buff[i]=(i+vizovy)&1;
    }
    vizovy++;
}
